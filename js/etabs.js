function showEtabs(commune){
	console.log(commune);
	var etabDiv = document.getElementById("setif__info");
	var isEmpty = etabDiv.innerHTML === "";
	if (!(isEmpty)){
		while(etabDiv.firstChild){
			etabDiv.removeChild(etabDiv.firstChild);
		}
	}
	if (commune == null){
		commune = "سطيف"
	}
	const headerDiv = document.createElement('header');
			etabDiv.appendChild(headerDiv);
			headerDiv.classList.add("text-center", "blue-baby", "setif__info--header");
			headerDiv.innerHTML = '<p> مؤسسات التكوين المهني و التمهين لبلدية '+commune+'</p>';
	for (var i = 0;i < etabs.length;i++){
		
		if (etabs[i].commune == commune){
			const myDiv = document.createElement('div');
			myDiv.classList.add("setif__info--etab");
			etabDiv.appendChild(myDiv);
			
			const div1 = document.createElement('div');
			myDiv.appendChild(div1);
			div1.classList.add("setif__info--img");
			const img = document.createElement('img');
			img.src = etabs[i].image;
			div1.appendChild(img);
			
			const div2= document.createElement('div');
			myDiv.appendChild(div2);
			div2.classList.add("setif__info--titles");
			
			
			const par1 = document.createElement("p");
			//par1.textContent = etabs[i].denomination;
			par1.innerHTML =  '<span class="fa-stack fa-lg"><i class="fa fa fa-institution fa-stack-1x"></i></span>'+etabs[i].denomination;
			div2.appendChild(par1);
			
			const par2 = document.createElement("p");
			div2.appendChild(par2);
			par2.innerHTML =  '<span class="fa-stack fa-lg"><i class="fa fa fa-address-book fa-stack-1x"></i></span>'+etabs[i].adresse;
			
			
			const par3 = document.createElement("p");
			div2.appendChild(par3);
			par3.innerHTML = '<span class="fa-stack fa-lg"><i class="fa fa-phone fa-stack-1x"></i></span>'+etabs[i].tel;
			
			const par4 = document.createElement("p");
			div2.appendChild(par4);
			par4.innerHTML = '<span class="fa-stack fa-lg"><i class="fa fa-fax fa-stack-1x"></i></span>'+etabs[i].fax;
			
			const par5 = document.createElement("p");
			div2.appendChild(par5);
			par5.innerHTML = '<span class="fa-stack fa-lg"><i class="fa fa-at fa-stack-1x"></i></span>' + etabs[i].email; 
			
			
		}
	}
}


function addCommLocalStore(param){
	
	if (typeof(Storage)!== "undefined") {
		localStorage.removeItem('lastCom');
		localStorage.setItem('lastCom',param);
	}

}

